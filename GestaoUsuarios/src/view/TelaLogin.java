    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        String nomeUsuario = txtUsuario.getText();
        String senha = txtSenha.getText();
        if (Login.login(nomeUsuario, senha)) {
            this.dispose();
            TelaPrincipal tela = new TelaPrincipal();
            tela.setVisible(true);
        } else {
            if (limite < 3) {
                JOptionPane.showMessageDialog(null, "Dados incorrectos, por favor verifique e volte a tentar!!");
                limite++;
            } else {
                JOptionPane.showMessageDialog(null, "Numero de tentativas extrapolado, programa encerrando...");
                this.dispose();
            }

        }
    }//GEN-LAST:event_btnEntrarActionPerformed

