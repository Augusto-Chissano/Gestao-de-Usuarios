    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String nomeUsuario = txtNomeUsuario.getText();
        String biografia = txtBiografia.getText();
        String senha = txtSenha.getText();
        String estadoCivil = jcEstadoCivil.getSelectedItem().toString();
        String morada = txtMorada.getText();
        String contacto = txtContacto.getText();

        // A instrucao a seguir pega o valor do campo senha 
        String confirmarSenha = txtConfirmar.getText();
        int numeroCaracteres = confirmarSenha.length();

        if (numeroCaracteres == 8) {

            if (CadastrarUsuario.cadastrarUsuario(nome, email, nomeUsuario, biografia, senha, estadoCivil, morada, contacto)) {
                JOptionPane.showMessageDialog(null, "Usuario Cadastrado!!");
                this.dispose();
                TelaLogin tela = new TelaLogin();
                tela.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar!!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "O campo senha deve conter 8 caracteres");

        }
    }//GEN-LAST:event_btnRegistarActionPerformed

