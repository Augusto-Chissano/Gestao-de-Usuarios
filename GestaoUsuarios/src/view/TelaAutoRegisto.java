    private void btnRegistarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistarMouseClicked
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String nomeUsuario = txtNomeUsuario.getText();
        String biografia = txtBiografia.getText();
        String senha = txtSenha.getText();
        String estadoCivil = jcEstadoCivil.getSelectedItem().toString();
        String morada = txtMorada.getText();
        String contacto = txtContacto.getText();
        String confirmar = txtConfirmar.getText();
        if (nome.isEmpty() && email.isEmpty() && nomeUsuario.isEmpty() && biografia.isEmpty() && senha.isEmpty() && estadoCivil.isEmpty() && morada.isEmpty() && contacto.isEmpty() && confirmar.isEmpty()) {
            btnRegistar.setEnabled(false);
        }
    }//GEN-LAST:event_btnRegistarMouseClicked

