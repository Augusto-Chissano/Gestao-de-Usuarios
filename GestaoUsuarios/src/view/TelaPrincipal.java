    private void preencherTabela() {
        String nome=  txtPesquisar.getText();
        this.controller = new UsuarioJpaController(ConnectionFactory.getEmf());
        List<Usuario> usuarios = this.controller.getUsuarioByLikeNome(nome);
        //pegando o modelo da tabela para que seja possivel manipular
        DefaultTableModel tabela = (DefaultTableModel) tbUsuarios.getModel();

        tabela.setNumRows(0);
        for (Usuario user : usuarios) {
            Object[] obj = new Object[]{
                user.getId(),
                user.getNome(),
                user.getEmail(),
                user.getNomeUsuario(),
                user.getBiografia(),
                user.getSenha(),
                user.getEstadoCivil(),
                user.getMorada(),
                user.getContacto()

            };
            tabela.addRow(obj);

        }

    }

    /**
     * Este metodo vai capturar os dados do user selecionado e preencher o
     * formulario
     */
    private void preencherCampos() {
        //pegando o numero da linha selecionada
        int linhaSelecionada = tbUsuarios.getSelectedRow();
        //caso nenhuma linha seja selecionada
        if (linhaSelecionada == -1) {
            System.out.println("selecione um usuario");
        } else {
            //pegando o primeiro valor da linha seleciona que eh o ID do usuario
            Long id = Long.parseLong(tbUsuarios.getValueAt(linhaSelecionada, 0).toString());
            //buscando o usario na base de dados pelo ID
            Usuario usuario = this.controller.findUsuario(id);
            //preencher os campos do formulario com os dados do usario encontrado
            txtBiografia.setText(usuario.getBiografia());
            txtContacto.setText(usuario.getContacto());
            txtEmail.setText(usuario.getEmail());
            txtId.setText(String.valueOf(usuario.getId()));
            txtMorada.setText(usuario.getMorada());
            txtNome.setText(usuario.getNome());
            txtSenha.setText(usuario.getSenha());
            txtNomeUsuario.setText(usuario.getNomeUsuario());
            this.habilitarSalvar(false);

        }

    }

    private void limparCampos() {

        txtBiografia.setText("");
        txtContacto.setText("");
        txtEmail.setText("");
        txtId.setText("");
        txtMorada.setText("");
        txtNome.setText("");
        txtSenha.setText("");
        txtNomeUsuario.setText("");
        this.habilitarSalvar(true);
    }

    private void habilitarSalvar(boolean opc) {
        if (opc == true) {
            btnSalvar.setEnabled(true);
            btnActualizar.setEnabled(false);
            btnExcluir.setEnabled(false);
        }else{
            btnSalvar.setEnabled(false);
            btnActualizar.setEnabled(true);
            btnExcluir.setEnabled(true);
        }

    }

