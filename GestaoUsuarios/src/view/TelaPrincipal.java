    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String nomeUsuario = txtNomeUsuario.getText();
        String biografia = txtBiografia.getText();
        String senha = txtSenha.getText();
        String estadoCivil = jcEstadoCivil.getSelectedItem().toString();
        String morada = txtMorada.getText();
        String contacto = txtContacto.getText();

        if (CadastrarUsuario.cadastrarUsuario(nome, email, nomeUsuario, biografia, senha, estadoCivil, morada, contacto)) {
            JOptionPane.showMessageDialog(null, "Usuario Cadastrado!!");
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar!!");
            this.limparCampos();
        }
        preencherTabela();
    }//GEN-LAST:event_btnSalvarActionPerformed

