    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        Long id = Long.parseLong(txtId.getText());
        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String nomeUsuario = txtNomeUsuario.getText();
        String biografia = txtBiografia.getText();
        String senha = txtSenha.getText();
        String estadoCivil = jcEstadoCivil.getSelectedItem().toString();
        String morada = txtMorada.getText();
        String contacto = txtContacto.getText();
        if (ActualizarUsuario.actualizarUsuario(id, nome, email, nomeUsuario, biografia, senha, estadoCivil, morada, contacto)) {
            JOptionPane.showMessageDialog(null, "Usuario Actualizado!!");
            this.limparCampos();
            this.preencherTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao actualizar!!");
            this.limparCampos();

        }


    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente eliminar o usuario?");

        if (resposta == 0) {
            Long id = Long.parseLong(txtId.getText());
            if (EliminarUsuario.eliminarUsuario(id)) {
                JOptionPane.showMessageDialog(null, "Usuario eliminado!!");
                this.limparCampos();
                this.preencherTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao eleminar usuario!!");
            }
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

